function getQuestionIndexMapping(filteredPages) {
    const mapping = {};
    let newIndex = 0;
    
    filteredPages.forEach(page => {
        page.questionIndices.forEach(originalIndex => {
            mapping[originalIndex] = newIndex;
            newIndex++;
        });
    });
    
    return mapping;
}

